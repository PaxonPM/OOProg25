
/// <summary>
/// The coin types that exist (we are in the year 2050...).
/// The value in Kr. of a coinType is equal to its actual numerical value,
/// i.e. kr5 is worth 5 kr., kr20 is worth 20 kr, and so on.
/// </summary>
public enum CoinType
{
    kr1 = 1,
    kr2 = 2,
    kr5 = 5,
    kr10 = 10,
    kr20 = 20,
    kr50 = 50,
    kr100 = 100
}
